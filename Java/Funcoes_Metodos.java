// 3. Funções e Métodos:

// Java (funções dentro de classes): Em Java, todas as funções (métodos) devem estar dentro de uma classe.

public class funcoes_metodos {
    public static void saudacao() {
        System.out.println("Olá de uma função em Java!");
        }
        public static void main(String[] args) {
        saudacao(); // Chamando o método
        }
}
