// 1. Declaração de Variáveis e Tipagem

// Java (tipagem forte): Em Java, você precisa declarar o tipo da variável, e ela não pode mudar de tipo ao longo do programa

public class variaveis_tipagem {
    public static void main(String[] args) {
    int numero = 10; //Declaração de uma variável inteira
    System.out.println(numero);
    // Não é possível fazer isso: número = "dez"; // Erro, pois "numero" é do tipo int
    }
}
